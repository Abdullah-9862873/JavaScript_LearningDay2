// Premitive DataType

let num1 = 6;
let num2 = num1;

console.log("num1", num1);
console.log("num2", num2);

num1++;
console.log("num1", num1);
console.log("num2", num2);

// Reference DataType

let array1 = ["item1", "item2", "item3"];
let array2 = array1;

console.log("array1", array1);
console.log("array2", array2);

array1.push("item4");
console.log("array1", array1);
console.log("array2", array2);